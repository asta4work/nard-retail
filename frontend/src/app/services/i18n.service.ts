@Injectable({ providedIn: 'root' })
export class I18nService {
  private readonly document = inject(DOCUMENT);
  readonly language = signal<AppLanguage>(this.readLanguage());
  readonly direction = signal<'ltr' | 'rtl'>(this.language() === 'ar' ? 'rtl' : 'ltr');

  constructor() {
    this.applyDocumentLanguage(this.language());
  }

  toggle() {
    this.setLanguage(this.language() === 'en' ? 'ar' : 'en');
  }

  setLanguage(language: AppLanguage) {
    localStorage.setItem('language', language);
    this.language.set(language);
    this.direction.set(language === 'ar' ? 'rtl' : 'ltr');
    this.applyDocumentLanguage(language);
  }

  translate(key: string): string {
    return this.language() === 'ar' ? ARABIC[key] ?? key : key;
  }

  get locale() {
    return this.language() === 'ar' ? 'ar-SA' : 'en-US';
  }

  private readLanguage(): AppLanguage {
    return localStorage.getItem('language') === 'ar' ? 'ar' : 'en';
  }

  private applyDocumentLanguage(language: AppLanguage) {
    this.document.documentElement.lang = language;
    this.document.documentElement.dir = language === 'ar' ? 'rtl' : 'ltr';
  }
}
